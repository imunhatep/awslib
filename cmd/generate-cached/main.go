func main() {
	moduleRoot := "."
	serviceDir := filepath.Join(moduleRoot, "service")

	entries, err := os.ReadDir(serviceDir)
	if err != nil {
		fatalf("read service dir: %v", err)
	}

	// Delete stale generated files before loading so they don't pollute alias maps
	cleanGeneratedFiles(serviceDir)

	tmpl, err := template.New("cached").Parse(cachedTemplate)
	if err != nil {
		fatalf("parse template: %v", err)
	}

	generated := 0

	for _, entry := range entries {
		if !entry.IsDir() {
			continue
		}

		pkgDir := filepath.Join(serviceDir, entry.Name())
		pkgPattern := "./" + pkgDir

		cfg := &packages.Config{
			Mode: packages.NeedName |
				packages.NeedFiles |
				packages.NeedSyntax |
				packages.NeedTypes |
				packages.NeedTypesInfo |
				packages.NeedImports,
			Fset: token.NewFileSet(),
		}

		pkgs, err := packages.Load(cfg, pkgPattern)
		if err != nil {
			fmt.Printf("⚠  skip %s: load error: %v\n", pkgDir, err)
			continue
		}
		if len(pkgs) == 0 {
			continue
		}

		pkg := pkgs[0]
		if len(pkg.Errors) > 0 {
			fmt.Printf("⚠  skip %s: package errors:\n", pkgDir)
			for _, e := range pkg.Errors {
				fmt.Printf("    %v\n", e)
			}
			continue
		}

		// Build alias map from non-generated source files only
		aliasMap := buildAliasMap(pkg)

		// Collect all Repository structs and their Get*/List* methods
		repoMethods := map[string][]MethodInfo{}
		repoRaws := map[string][]rawMethod{}

		scope := pkg.Types.Scope()
		for _, name := range scope.Names() {
			obj := scope.Lookup(name)
			if obj == nil {
				continue
			}
			typeName, ok := obj.(*types.TypeName)
			if !ok || !typeName.Exported() || !strings.HasSuffix(name, "Repository") {
				continue
			}
			named, ok := typeName.Type().(*types.Named)
			if !ok {
				continue
			}
			if _, ok := named.Underlying().(*types.Struct); !ok {
				continue
			}

			ptrType := types.NewPointer(named)
			mset := types.NewMethodSet(ptrType)
			for i := 0; i < mset.Len(); i++ {
				sel := mset.At(i)
				fn, ok := sel.Obj().(*types.Func)
				if !ok || !fn.Exported() {
					continue
				}
				if !strings.HasPrefix(fn.Name(), "Get") && !strings.HasPrefix(fn.Name(), "List") {
					continue
				}
				if fn.Name() == "GetRegion" || fn.Name() == "GetAccountID" {
					continue
				}

				sig := fn.Type().(*types.Signature)
				var params []ParamInfo
				for j := 0; j < sig.Params().Len(); j++ {
					p := sig.Params().At(j)
					params = append(params, ParamInfo{
						Name: paramName(p.Name(), j),
						Type: typeString(p.Type(), pkg.PkgPath, aliasMap),
					})
				}

				var results []ParamInfo
				anyChan := false
				for j := 0; j < sig.Results().Len(); j++ {
					r := sig.Results().At(j)
					ts := typeString(r.Type(), pkg.PkgPath, aliasMap)
					results = append(results, ParamInfo{
						Name: fmt.Sprintf("r%d", j),
						Type: ts,
					})
					if hasChan(r.Type()) {
						anyChan = true
					}
				}
				if len(results) == 0 {
					continue
				}

				repoMethods[name] = append(repoMethods[name], MethodInfo{
					Name:    fn.Name(),
					Params:  params,
					Results: results,
					HasChan: anyChan,
				})
				repoRaws[name] = append(repoRaws[name], rawMethod{name: fn.Name(), sig: sig})
			}
		}

		if len(repoMethods) == 0 {
			continue
		}

		var repoNames []string
		for n := range repoMethods {
			repoNames = append(repoNames, n)
		}
		sort.Strings(repoNames)

		for _, repoName := range repoNames {
			methods := repoMethods[repoName]
			sort.Slice(methods, func(i, j int) bool { return methods[i].Name < methods[j].Name })

			info := RepoInfo{
				Package:      pkg.Name,
				ImportPath:   pkg.PkgPath,
				RepoName:     repoName,
				Methods:      methods,
				ExtraImports: collectRequiredImports(repoRaws[repoName], pkg, aliasMap),
			}

			outPath := filepath.Join(pkgDir, "cached_repository.go")
			if len(repoNames) > 1 {
				safeName := strings.ToLower(strings.TrimSuffix(repoName, "Repository"))
				outPath = filepath.Join(pkgDir, "cached_"+safeName+"_repository.go")
			}

			var buf bytes.Buffer
			if err := tmpl.Execute(&buf, info); err != nil {
				fatalf("execute template for %s/%s: %v", pkg.Name, repoName, err)
			}

			formatted, err := format.Source(buf.Bytes())
			if err != nil {
				_ = os.WriteFile(outPath, buf.Bytes(), 0644)
				fmt.Printf("⚠  %s/%s: format error (raw file written): %v\n", pkg.Name, repoName, err)
				continue
			}

			if err := os.WriteFile(outPath, formatted, 0644); err != nil {
				fatalf("write %s: %v", outPath, err)
			}

			fmt.Printf("✓  generated %s\n", outPath)
			generated++
		}
	}

	fmt.Printf("\n✓  Total: %d file(s) generated\n", generated)
}

// cleanGeneratedFiles removes previously generated cached_*_repository.go
// files so stale content does not pollute alias maps on re-runs.
func cleanGeneratedFiles(serviceDir string) {
	_ = filepath.WalkDir(serviceDir, func(path string, d os.DirEntry, err error) error {
		if err != nil || d.IsDir() {
			return err
		}
		name := d.Name()
		if strings.HasPrefix(name, "cached_") && strings.HasSuffix(name, "_repository.go") {
			_ = os.Remove(path)
			return nil
		}
		if name == "cached_repository.go" {
			_ = os.Remove(path)
		}
		return nil
	})
}

func paramName(name string, idx int) string {
	if name == "" || name == "_" {
		return fmt.Sprintf("p%d", idx)
	}
	return name
}

func fatalf(format string, args ...interface{}) {
	fmt.Fprintf(os.Stderr, "error: "+format+"\n", args...)
	os.Exit(1)
}
