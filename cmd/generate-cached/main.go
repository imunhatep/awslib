//go:build ignore

// generate-cached scans every package under service/, finds all exported
// *Repository structs and their Get*/List* methods, then emits a
// *RepositoryCached wrapper per package that transparently reads/writes
// through a *cache.DataCache.
//
// Usage:
//
//	go run ./cmd/generate-cached
package main

import (
	"bytes"
	"fmt"
	"go/ast"
	"go/format"
	"go/token"
	"go/types"
	"os"
	"path/filepath"
	"sort"
	"strings"
	"text/template"

	"golang.org/x/tools/go/packages"
)

// --------------------------------------------------------------------------
// Data model
// --------------------------------------------------------------------------

type ParamInfo struct {
	Name string
	Type string
}

type MethodInfo struct {
	Name    string
	Params  []ParamInfo
	Results []ParamInfo
	// whether ANY result type contains a channel (skip caching for those)
	HasChan bool
}

// CacheKey returns the Go expression for building the cache key inside the
// wrapper method body. For parameter-less methods: just the method name.
// For methods with params: fnv32 hash of "<MethodName>:<fmt.Sprintf params...>".
func (m MethodInfo) CacheKey() string {
	if len(m.Params) == 0 {
		return fmt.Sprintf("%q", m.Name)
	}
	sprintfArgs := make([]string, len(m.Params))
	for i, p := range m.Params {
		sprintfArgs[i] = fmt.Sprintf("fmt.Sprintf(\"%%+v\", %s)", p.Name)
	}
	raw := fmt.Sprintf(`fmt.Sprintf("%s:%%s", strings.Join([]string{%s}, ":"))`,
		m.Name, strings.Join(sprintfArgs, ", "))
	return fmt.Sprintf("cachedRepoHashKey(%s)", raw)
}

// ResultVars returns comma-separated result variable names (r0, r1, …).
func (m MethodInfo) ResultVars() string {
	var names []string
	for i := range m.Results {
		names = append(names, fmt.Sprintf("r%d", i))
	}
	return strings.Join(names, ", ")
}

// ResultTypes returns comma-separated result types.
func (m MethodInfo) ResultTypes() string {
	var ts []string
	for _, r := range m.Results {
		ts = append(ts, r.Type)
	}
	return strings.Join(ts, ", ")
}

// ParamNames returns comma-separated param variable names for call sites.
func (m MethodInfo) ParamNames() string {
	var names []string
	for _, p := range m.Params {
		names = append(names, p.Name)
	}
	return strings.Join(names, ", ")
}

// ParamSignature returns the full param list for the method signature.
func (m MethodInfo) ParamSignature() string {
	var parts []string
	for _, p := range m.Params {
		parts = append(parts, p.Name+" "+p.Type)
	}
	return strings.Join(parts, ", ")
}

// CacheableResult is the first non-error result (used as the cached value).
func (m MethodInfo) CacheableResult() string {
	for i, r := range m.Results {
		if r.Type != "error" {
			return fmt.Sprintf("r%d", i)
		}
	}
	return "r0"
}

// CacheableResultType is the type of the cacheable result.
func (m MethodInfo) CacheableResultType() string {
	for _, r := range m.Results {
		if r.Type != "error" {
			return r.Type
		}
	}
	return m.Results[0].Type
}

// ErrorVar returns the variable name of the error result.
func (m MethodInfo) ErrorVar() string {
	for i, r := range m.Results {
		if r.Type == "error" {
			return fmt.Sprintf("r%d", i)
		}
	}
	return "_"
}

// HasError reports whether the method returns an error.
func (m MethodInfo) HasError() bool {
	for _, r := range m.Results {
		if r.Type == "error" {
			return true
		}
	}
	return false
}

// HasParams reports whether the method has any parameters.
func (m MethodInfo) HasParams() bool { return len(m.Params) > 0 }

// ImportSpec holds a verbatim import spec copied from the source file.
type ImportSpec struct {
	Alias string // explicit alias (may be empty or "_")
	Path  string // import path without quotes
}

type RepoInfo struct {
	Package      string // e.g. "ec2"
	ImportPath   string // e.g. "github.com/imunhatep/awslib/service/ec2"
	RepoName     string // e.g. "Ec2Repository"
	Methods      []MethodInfo
	ExtraImports []ImportSpec // imports copied verbatim from source files
}

// rawMethod holds the unrendered types.Func for import-path walking.
type rawMethod struct {
	name string
	sig  *types.Signature
}

// --------------------------------------------------------------------------
// Template
// --------------------------------------------------------------------------

const cachedTemplate = `// Code generated by generate-cached. DO NOT EDIT.
package {{.Package}}

import (
	"fmt"
	"hash/fnv"
	"strings"

	"github.com/imunhatep/awslib/cache"
{{- range .ExtraImports}}
	{{if .Alias}}{{.Alias}} {{end}}"{{.Path}}"
{{- end}}
)

// cachedRepoHashKey returns a short, file-safe FNV-32 hex hash of the given string,
// prefixed by the method name component for readability.
func cachedRepoHashKey(raw string) string {
	h := fnv.New32a()
	_, _ = h.Write([]byte(raw))
	return fmt.Sprintf("%x", h.Sum32())
}

// {{.RepoName}}Cached wraps {{.RepoName}} and caches results of Get*/List* calls.
type {{.RepoName}}Cached struct {
	repo  *{{.RepoName}}
	cache *cache.DataCache
}

// WithCache returns a {{.RepoName}}Cached that stores/retrieves results via the given DataCache.
// The cache namespace is set to "<accountID>:<region>".
func (r *{{.RepoName}}) WithCache(dc *cache.DataCache) *{{.RepoName}}Cached {
	ns := fmt.Sprintf("%s:%s", r.client.GetAccountID(), r.client.GetRegion())
	return &{{.RepoName}}Cached{
		repo:  r,
		cache: dc.WithNamespace(ns),
	}
}
{{range .Methods}}
{{- if .HasChan}}
// {{.Name}} delegates directly to the underlying repository (channel return – not cached).
func (c *{{$.RepoName}}Cached) {{.Name}}({{.ParamSignature}}) ({{.ResultTypes}}) {
	return c.repo.{{.Name}}({{.ParamNames}})
}
{{- else}}
// {{.Name}} returns cached results when available, otherwise delegates to the underlying repository.
func (c *{{$.RepoName}}Cached) {{.Name}}({{.ParamSignature}}) ({{.ResultTypes}}) {
	_ = strings.Join // used by cachedRepoHashKey helper when params are present
	cacheKey := {{.CacheKey}}
	var cached {{.CacheableResultType}}
	if c.cache.Read(cacheKey, &cached) {
		return cached{{if .HasError}}, nil{{end}}
	}
	{{.ResultVars}} := c.repo.{{.Name}}({{.ParamNames}})
{{- if .HasError}}
	if {{.ErrorVar}} == nil {
		_ = c.cache.Write(cacheKey, {{.CacheableResult}})
	}
{{- end}}
	return {{.ResultVars}}
}
{{- end}}
{{end}}`

