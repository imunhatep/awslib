func zeroValue(t string) string {
	switch {
	case t == "error":
		return "nil"
	case strings.HasPrefix(t, "[]") || strings.HasPrefix(t, "map[") ||
		strings.HasPrefix(t, "*") || strings.HasPrefix(t, "<-chan") ||
		strings.HasPrefix(t, "chan"):
		return "nil"
	case t == "bool":
		return "false"
	case t == "int" || t == "int32" || t == "int64" ||
		t == "uint" || t == "uint32" || t == "uint64":
		return "0"
	case t == "string":
		return `""`
	default:
		return fmt.Sprintf("%s{}", t)
	}
}

// typeString converts a types.Type to a Go source string.
// It uses the provided alias map (pkgPath → local alias) to render qualifiers
// exactly as they appear in the original source files.
func typeString(t types.Type, pkgPath string, aliasMap map[string]string) string {
	switch v := t.(type) {
	case *types.Named:
		obj := v.Obj()
		if obj.Pkg() == nil || obj.Pkg().Path() == pkgPath {
			return obj.Name()
		}
		path := obj.Pkg().Path()
		if alias, ok := aliasMap[path]; ok && alias != "" {
			return alias + "." + obj.Name()
		}
		return obj.Pkg().Name() + "." + obj.Name()
	case *types.Pointer:
		return "*" + typeString(v.Elem(), pkgPath, aliasMap)
	case *types.Slice:
		return "[]" + typeString(v.Elem(), pkgPath, aliasMap)
	case *types.Map:
		return "map[" + typeString(v.Key(), pkgPath, aliasMap) + "]" + typeString(v.Elem(), pkgPath, aliasMap)
	case *types.Chan:
		switch v.Dir() {
		case types.RecvOnly:
			return "<-chan " + typeString(v.Elem(), pkgPath, aliasMap)
		case types.SendOnly:
			return "chan<- " + typeString(v.Elem(), pkgPath, aliasMap)
		default:
			return "chan " + typeString(v.Elem(), pkgPath, aliasMap)
		}
	case *types.Basic:
		return v.Name()
	case *types.Interface:
		if v.Empty() {
			return "interface{}"
		}
		return v.String()
	default:
		return t.String()
	}
}

func hasChan(t types.Type) bool {
	switch v := t.(type) {
	case *types.Chan:
		return true
	case *types.Pointer:
		return hasChan(v.Elem())
	default:
		_ = v
		return false
	}
}

// buildAliasMap builds pkgPath → localAlias from all NON-GENERATED source
// files in the package.
// Rule: an explicit alias always beats a no-alias entry, regardless of file
// processing order. "" means "use pkg.Name()".
func buildAliasMap(pkg *packages.Package) map[string]string {
	m := map[string]string{}
	for _, f := range pkg.Syntax {
		// Skip generated files to avoid picking up stale aliases
		if isGeneratedFile(pkg.Fset, f) {
			continue
		}
		for _, imp := range f.Imports {
			path := strings.Trim(imp.Path.Value, `"`)
			alias := ""
			if imp.Name != nil && imp.Name.Name != "_" && imp.Name.Name != "." {
				alias = imp.Name.Name
			}
			prev, exists := m[path]
			if !exists || (alias != "" && prev == "") {
				// First time seeing this path, or we now have an explicit alias
				// that upgrades a previously-recorded no-alias entry.
				m[path] = alias
			}
		}
	}
	return m
}

// isGeneratedFile reports whether an AST file was produced by a code generator
// (contains the "DO NOT EDIT" marker in its first comment group).
func isGeneratedFile(fset *token.FileSet, f *ast.File) bool {
	for _, cg := range f.Comments {
		for _, c := range cg.List {
			if strings.Contains(c.Text, "DO NOT EDIT") {
				return true
			}
		}
		// Only check the first comment group near the top of the file
		if fset.Position(cg.Pos()).Line > 5 {
			break
		}
	}
	return false
}

// collectRequiredImports walks the raw method signatures, collects all
// external package paths referenced, then returns verbatim ImportSpec values
// copied from the source files' AST import specs.
//
// Excluded:
//   - the package itself
//   - internal client helpers  (provider/v3/clients/…)
//   - cache package (hard-coded in template)
func collectRequiredImports(
	raws []rawMethod,
	pkg *packages.Package,
	aliasMap map[string]string,
) []ImportSpec {
	// 1. Walk types to collect needed package paths
	needed := map[string]bool{}
	var walk func(t types.Type)
	walk = func(t types.Type) {
		switch v := t.(type) {
		case *types.Named:
			p := v.Obj().Pkg()
			if p == nil || p.Path() == pkg.PkgPath {
				return
			}
			needed[p.Path()] = true
		case *types.Pointer:
			walk(v.Elem())
		case *types.Slice:
			walk(v.Elem())
		case *types.Map:
			walk(v.Key())
			walk(v.Elem())
		case *types.Chan:
			walk(v.Elem())
		}
	}
	for _, rm := range raws {
		for i := 0; i < rm.sig.Params().Len(); i++ {
			walk(rm.sig.Params().At(i).Type())
		}
		for i := 0; i < rm.sig.Results().Len(); i++ {
			walk(rm.sig.Results().At(i).Type())
		}
	}

	// 2. Build verbatim import specs from non-generated source files.
	// Explicit alias always beats a no-alias entry for the same path.
	specByPath := map[string]ImportSpec{}
	for _, f := range pkg.Syntax {
		if isGeneratedFile(pkg.Fset, f) {
			continue
		}
		for _, imp := range f.Imports {
			path := strings.Trim(imp.Path.Value, `"`)
			alias := ""
			if imp.Name != nil && imp.Name.Name != "_" && imp.Name.Name != "." {
				alias = imp.Name.Name
			}
			prev, exists := specByPath[path]
			if !exists || (alias != "" && prev.Alias == "") {
				specByPath[path] = ImportSpec{Alias: alias, Path: path}
			}
		}
	}

	// 3. Filter to only paths actually needed and not excluded
	var out []ImportSpec
	for path := range needed {
		if strings.Contains(path, "provider/v3/clients") {
			continue
		}
		if path == "github.com/imunhatep/awslib/cache" {
			continue
		}
		spec, ok := specByPath[path]
		if !ok {
			// Fallback: no verbatim spec found, emit path only
			spec = ImportSpec{Path: path}
		}
		out = append(out, spec)
	}

	sort.Slice(out, func(i, j int) bool { return out[i].Path < out[j].Path })
	return out
}

